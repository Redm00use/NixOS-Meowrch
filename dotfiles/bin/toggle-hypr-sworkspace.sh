# Check if the active window is on a special workspace
active_ws=$(hyprctl -j activewindow 2>/dev/null | jq -r '.workspace.name // ""')

if [[ "$active_ws" == special* ]]; then
    # Move back to the last regular workspace
    target_ws=$(hyprctl -j activeworkspace 2>/dev/null | jq -r '.id // 1')
    # If we're on a special workspace, move window to workspace 1 (fallback)
    hyprctl dispatch movetoworkspace 1
else
    hyprctl dispatch movetoworkspacesilent special
fi
